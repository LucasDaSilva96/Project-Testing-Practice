export function add(number_1, number_2) {
  return number_1 + number_2;
}

export function subtract(number_1, number_2) {
  return number_1 - number_2;
}

export function divide(number_1, number_2) {
  return number_1 / number_2;
}

export function multiply(number_1, number_2) {
  return number_1 * number_2;
}
